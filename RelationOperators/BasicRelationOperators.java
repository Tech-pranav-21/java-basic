public class BasicRelationOperators {
    public static void main(String[] args)
    {
        int a = 20;
        a += 10 ;
        System.out.println(a);

        int b = 20;
        b -= 5 ;
        System.out.println(b);

        int c = 20;
        c *= 5 ;
        System.out.println(c);

        int d = 20;
        d /= 5 ;
        System.out.println(d);

        float e = 20f;
        e %= 5 ;
        System.out.println(e);

    }
}
